package java基础.ArrayListDemo;

public class Stydu {
    private String name;
    private Integer age;

    public Stydu() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public Stydu(String name, Integer age) {
        this.name = name;
        this.age = age;
    }
}
